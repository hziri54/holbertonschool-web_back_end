#!/usr/bin/env python3
def to_str(n: float) -> str:
    """
    Prend un nombre flottant n et renvoie sa représentation sous forme de chaîne de caractères.
    
    Args:
        n (float): Le nombre flottant à convertir.
    
    Returns:
        str: La chaîne de caractères représentant n.
    """
    return str(n)
